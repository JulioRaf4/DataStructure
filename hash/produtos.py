class Teclado:
    def __init__(self, price, name):
        self.price = price
        self.name = name
    
    def get_price(self):
        return self._price
    
    def set_price(self, new_price):
        self._price = new_price

    def get_name(self):
        return self._name

    def set_name(self, new_name):
        self._name = new_name

    def __str__(self):
        return f"Teclado: {self._name}, Preço: R${self._price:.2f}"
        
